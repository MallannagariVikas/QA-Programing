using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _2_Swap
{
    class Program
    {
        static void Main(string[] args)
        {
            int FirstNumber = 5;
            int SecondNumber = 6;

            if (Convert.ToBoolean(FirstNumber = 5))
            {
                Console.WriteLine("\n FirstNumber=" + SecondNumber);
            }
            if (Convert.ToBoolean(SecondNumber = 6))
            {
                Console.WriteLine("\n SecondNumber=" + FirstNumber);
            }
        }
    }
}
